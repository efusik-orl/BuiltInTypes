# Запускаем бесконечный цикл
while True:
    # Запрашиваем у пользователя ввод чисел, разделенных запятой
    numbers_str = input("Введите числа, разделенные запятой: ")
    # Проверяем, что все элементы списка являются числами
    if all(num.isdigit() for num in numbers_str.split(",")):
        # Если условие выполнено, преобразуем строку в список и кортеж
        numbers_list = numbers_str.split(",")
        numbers_tuple = tuple(numbers_list)
        # Выводим результаты и выходим из цикла
        print("Список:", numbers_list)
        print("Кортеж:", numbers_tuple)
        break
    else:
        # Если условие не выполнено, выводим сообщение об ошибке и продолжаем цикл
        print("Ошибка! Введите только числа, разделенные запятой.")
